    // M√©todo para devolver la bicicleta a la base de datos
private void devolverBicicletaABaseDeDatos(Bicicleta bicicleta) {
    Connection conn = null;
    PreparedStatement ps = null;
    Conexion conexion = null;
    try {
        conn = conexion.getConexion();
        String query = "INSERT INTO bicicletas (color, numero, estacion) VALUES (?, ?, ?)";
        ps = conn.prepareStatement(query);
        ps.setString(1, bicicleta.getColor());
        ps.setInt(2, bicicleta.getNumero());
        ps.setString(3, bicicleta.getEstacion());
        ps.executeUpdate();
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        try {
            // Cerrar recursos
            if (ps != null) ps.close();
            if (conn != null) conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}

    private Bicicleta obtenerBicicletaParaEntregar(String estacionSeleccionada) {
    // Implementa la l√≥gica para obtener la bicicleta que se va a devolver en la estaci√≥n seleccionada
    for (Bicicleta bicicleta : bicicletasEnEstacion) {
        if (bicicleta.getEstacion().equals(estacionSeleccionada)) {
            return bicicleta;
        }
    }
    return null;
}
        
     public ArrayList<Bicicleta> obtenerBicicletas(String estacionSeleccionada) {
        PreparedStatement ps = null;
        ResultSet rs = null;
        PreparedStatement ps2 = null;
        ResultSet rs2 = null;

        try (Connection conn = Conexion.getConexion()) {
            String query = "SELECT * FROM bicicletas WHERE estacion = ?";
            String query2 = "SELECT * FROM personas LIMIT 1"; // Obtener la primera persona
            ps = conn.prepareStatement(query);
            ps.setString(1, estacionSeleccionada);
            rs = ps.executeQuery();

            if (rs.next()) {
                Bicicleta bicicleta = new Bicicleta();
                bicicleta.setColor(rs.getString("color"));
                bicicleta.setNumero(rs.getInt("numero"));

                // Obtener la informaci√≥n de la primera persona
                ps2 = conn.prepareStatement(query2);
                rs2 = ps2.executeQuery();

                if (rs2.next()) {
                    Persona persona = new Persona();
                    persona.setNombre(rs2.getString("nombre")+" "+rs2.getString("apellido"));
                    bicicleta.setPerson(persona);
                }

                bicicleta.setEstacion(estacionSeleccionada);
                bicicletasEnEstacion.add(bicicleta);
                eliminarBicicleta(rs.getInt("numero"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } return bicicletasEnEstacion;
     } 
     
     private void eliminarBicicleta(int numBicicleta) {
    PreparedStatement ps = null;

    try (Connection conn = Conexion.getConexion()) {
        String query = "DELETE FROM bicicletas WHERE numero = ?";
        ps = conn.prepareStatement(query);
        ps.setInt(1, numBicicleta);
        ps.executeUpdate();
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        try (Connection conn = Conexion.getConexion()) {
            // Cerrar recursos
            if (ps != null) ps.close();
            if (conn != null) conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}
